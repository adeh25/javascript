// Balok

let panjang = 8;
let lebar = 6;
let tinggi = 3;

const balok = panjang * lebar * tinggi;
console.log("volume balok : " + balok);

// Kubus

let s1 = 5;
let s2 = 9;
let s3 = 2;

const kubus = s1 * s2 * s3;
console.log("volume kubus : " + kubus);

// Tabung

const phi = 3.14;
let r = 3;
let t = 6;

const tabung = phi * (r * r) * t;
console.log("volume tabung : " + tabung);

// Kerucut

const PHI = 3.14;
let r1 = 5;
let r2 = 9;
let tnggi = 3;

const kerucut = (1 / 3) * PHI * r1 * r2 * tnggi;
console.log("volume kerucut : " + kerucut);

// Bola

const ph = 3.14;

const bola = (4 / 3) * ph * ph * ph;
console.log("volume bola : " + bola);
